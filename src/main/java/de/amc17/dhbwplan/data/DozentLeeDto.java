package de.amc17.dhbwplan.data;

public class DozentLeeDto {

	private int dozid;
	
	private int leid;
	
	private String name;
	
	private String ModulName;
	
	public int getDozid() {
		return dozid;
	}
	
	public void setDozid(int dozid) {
		this.dozid = dozid;
	}
	
	public int getLeid() {
		return leid;
	}
	
	public void setLeid(int leid) {
		this.leid = leid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getModulName() {
		return ModulName;
	}

	public void setModulName(String modulName) {
		ModulName = modulName;
	}
	
	
	
}
